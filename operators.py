# comparison operators

a=12
b=34

print(f"{a} is equal to {b} {a==b}")
print(f"{a} is not equal to {b} {a!=b}")
print(f"{a} is greater than or equal to {b} {a>=b}")
print(f"{a} is less than or equals to {b} {a<=b}")


